                            <p><b style="color:var( --red-text);">Flexible Learning Options:</b> <i style="color:var( --red-text);">Alternative Learning System – Education Skills and Training</i><br><br>

                                The Alternative Learning System (ALS) Integrated Education and Skills Training Program modifies the regular ALS program by integrating Technical-Vocational Training and other skills training. The program will utilize existing DepEd expertise, particularly from remaining technical high schools and select Senior High Schools, and leverage partnerships with SUCs, LGUS, private sector, and CSOs, to provide the technical-vocational and other skills training components attuned to the demands and opportunities of the local community and the country.<br><br>

                                The ALS Integrated Education and Skills Training Program aims to produce ALS completers who are not only able to catch up with basic academic education but have also acquired technical competencies suitable for immediate employment.</p>
